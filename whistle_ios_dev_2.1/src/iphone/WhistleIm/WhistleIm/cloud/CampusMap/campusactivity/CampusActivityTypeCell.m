@implementation CampusActivityTypeCell

- (id)initWithFrame:(CGRect)frame
{
    self = [super initWithFrame:frame];
    if (self) {
        // Initialization code
        self.typeNameLabel = [[UILabel alloc] initWithFrame:CGRectMake(66, 15, frame.size.width/2, 14)];
        UIFont *font = [UIFont fontWithName:@"Arial Rounded MT Bold" size:14.0];
        self.typeNameLabel.font = font;
        [self.contentView addSubview:self.typeNameLabel];
        
        self.typeIcon = [[UIImageView alloc] initWithFrame:CGRectMake(36, 13, 18, 18)];
        [self.contentView addSubview:self.typeIcon];
        self.backgroundColor = [UIColor colorWithRed:240.0/255 green:240.0/255 blue:240.0/255 alpha:1.0];
    }
    return self;
}

